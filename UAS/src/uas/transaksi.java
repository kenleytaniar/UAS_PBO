        public void update(String tanggal, int harga, int lama, String jenis){
        try {
            Class.forName(JDBC_DRIVER);
   
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
 
            stmt = conn.createStatement();
            
            String sql = "UPDATE tsaksi SET tanggal=?, harga=?, lama=?, jenis=? WHERE tanggal='"+tanggal+"'";
            
            PreparedStatement pms = conn.prepareStatement(sql);
            pms.setString(1, tanggal);
            pms.setInt(2, harga);
            pms.setInt(3, lama);
            pms.setString(4, jenis);
            
            
            pms.execute();
            
            stmt.close();
            conn.close();
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
        public void showtable(){
        try{
            Class.forName(JDBC_DRIVER);	   
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No");
            model.addColumn("Tanggal Transaksi");
            model.addColumn("Harga ");
            model.addColumn("Lama ");
            model.addColumn("Jenis Kamar");
            
 
            stmt = conn.createStatement();
            String sql = "SELECT * FROM tsaksi";
            int i = 1;
            rs = stmt.executeQuery(sql);
            while(rs.next()) {
            	model.addRow(new Object[] {
                        i,
            		rs.getString("tanggal"),
            		rs.getInt("harga"),
            		rs.getInt("lama"),
            		rs.getString("jenis"),
                        
            	});
            	i++;
            }
            rs.close();
            conn.close();
            stmt.close();
            
            tabeldata.setModel(model);
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
