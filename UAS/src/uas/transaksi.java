    public void insert(String tanggal, int harga, int lama, String jenis){
        try {
            Class.forName(JDBC_DRIVER);
   
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
 
            stmt = conn.createStatement();
            
            String sql = "INSERT INTO tsaksi (tanggal, harga, lama, jenis ) VALUES (?,?,?,?)";
            
            PreparedStatement pms = conn.prepareStatement(sql);
            pms.setString(1, tanggal);
            pms.setInt(2, harga);
            pms.setInt(3, lama);
            pms.setString(4, jenis);
            
            
            pms.execute();
            
            stmt.close();
            conn.close();
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
