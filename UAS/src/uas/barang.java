        public void update(String kode, int nomor, String jenis, int harga){
        try {
            Class.forName(JDBC_DRIVER);
   
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
 
            stmt = conn.createStatement();
            
            String sql = "UPDATE tkamar SET kode=?, nomor=?, jenis=?, harga=? WHERE kode='"+kode+"'";
            
            PreparedStatement pms = conn.prepareStatement(sql);
            pms.setString(1, kode);
            pms.setInt(2, nomor);
            pms.setString(3, jenis);
            pms.setInt(4, harga);
            
            
            pms.execute();
            
            stmt.close();
            conn.close();
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
        public void showtable(){
        try{
            Class.forName(JDBC_DRIVER);	   
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No");
            model.addColumn("Kode Barang");
            model.addColumn("nomor Barang");
            model.addColumn("Jenis Barang");
            model.addColumn("Harga Barang");
            
 
            stmt = conn.createStatement();
            String sql = "SELECT * FROM tkamar";
            int i = 1;
            rs = stmt.executeQuery(sql);
            while(rs.next()) {
            	model.addRow(new Object[] {
                        i,
            		rs.getString("kode"),
            		rs.getInt("nomor"),
            		rs.getString("jenis"),
            		rs.getInt("harga"),
                        
            	});
            	i++;
            }
            rs.close();
            conn.close();
            stmt.close();
            
            tabeldata.setModel(model);
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
